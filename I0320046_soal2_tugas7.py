# Fungsi floor
import math
nilai = 86.0
print ("Hasan mendapatkan nilai Fisika sebesar", math.floor(nilai), "pada ujian akhir semester 2")

# Fungsi min dan max
import math
Progkom = 90
Fisika = 86
Kalkulus = 92
print ("Nilai tertinggi Hasan pada semester ini ialah ", max(Progkom,Fisika,Kalkulus), ", yaitu pada mata kuliah Kalkulus")
print ("Nilai terendah Hasan pada semester ini ialah ", min(Progkom,Fisika,Kalkulus), ", yaitu pada mata kuliah Fisika")

# Fungsi sqrt
import math
Alas = 20
Tinggi = 30
S_miring = Alas ** 2 + Tinggi ** 2
print ("Hasan ditugaskan oleh Ayah untuk menghitung sisi miring dinding kamar adik untuk membuat hiasan dinding, \nhasil perhitungan menyatakan bahwa besar sisi miring dinding adik ialah",math.sqrt(S_miring),"m")